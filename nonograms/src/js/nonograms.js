export function generateSVG(arr) {
  const size = arr.length * 8;
  const pre = `<svg
  viewBox="0 0 ${size} ${size}"
  version="1.1"
  id="svg1"
  xmlns="http://www.w3.org/2000/svg">`;
  let result = pre;
  const post = '\n</svg>';
  for (let row = 0; row < arr.length; row += 1) {
    for (let column = 0; column < arr.length; column += 1) {
      if (arr[row][column]) {
        result += `
    <rect
      id="rect-${row}-${column}"
      width="8"
      height="8"
      x="${column * 8}"
      y="${row * 8}" />`
      }
    }
  }
  result += post;
  return result;
}